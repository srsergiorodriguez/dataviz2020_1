                <br> <br>
                Respecto a la noción de duelo, este concepto se nutre de dos grandes contextos. Por una parte, el contexto de la violencia política es más que suficiente para retratar el drama de familiares de las víctimas. De otro lado, las calamidades individuales o la vida cotidiana también tienen escenas que la artista intenta retratar sobre el proceso de la pérdida de un ser querido. En suma, el duelo es un proceso que trasciende cualquier hecho de índole violenta o la muerte.
