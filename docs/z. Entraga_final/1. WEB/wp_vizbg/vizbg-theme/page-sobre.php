                Por tal motivo, la selección de varios hechos históricos del contexto colombiano es necesaria para no desvincular a la artista del contexto en que vivió e intervino. La obra de Beatriz González no puede desmarcarse de las realidades nacionales de la segunda mitad del siglo XX. En ese orden, los hechos históricos tienen un vínculo inherente con la violencia política colombiana. Esta selección de hechos también dio cuenta de una nueva problematización sobre la visibilización del espectro temático y discursivo de Beatriz González. La correlación de los hechos históricos y las obras se marcó a partir de un dato booleano (true/false).
